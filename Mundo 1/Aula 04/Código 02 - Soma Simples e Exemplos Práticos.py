""" NOTA: Números são usados primordialmente para cálculos, e portanto, não usam aspas dentro dos parênteses,
exceto se a ideia for realmente juntá-los ao invés de calculá-los. """

print(7 + 4)  # 7 + 4 = 11

print('7' + '4')  # '7' + '4' = '74'

print('7', '4')  # '7' , '4' = '7 4'

""" NOTA: Uma mensagem e um número é possivel no mesmo print utilizando a vírgula, mas não o símbolo de "+".
Usando a vírgula, adiciona-se um espaço entre os valores dentro do print."""

print('Olá', 5)  # 'Olá' , 5 = 'Olá 5'
